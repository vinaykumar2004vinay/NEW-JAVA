//Switch Statements
class five
{
    public static void main(String args[])
    {
        int number = 40;
        switch(number)              //Switch Expression
        {
            //Case Statements
            case 10:
            System.out.println("10");
            break;
            case 20:
            System.out.println("20");
            break;
            case 30:
            System.out.println("30");
            //Default Statement
            default:
            System.out.println("Not Available");
        }
    }
}