public class Unary 
{
    public static void main(String args[])
    {
        int x = 10;
        int y = 10;
        System.out.println("PostIncrement : " +  (x++));
        System.out.println("PreIncrement  : "  +  (++x));
        System.out.println("PostIncrement : " +  (y--));
        System.out.println("PreIncrement  : "  +  (--y));
    }
}