public class Ternary
{
    public static void main(String args[])
    {
        int n1 = 5 , n2 = 10 , max;
        System.out.println("First number :" + (n1));
        System.out.println("Second number :" + (n2));
        max=(n1>n2)?n1:n2;
        System.out.println("Maximum Number :  " +max);
    }
}