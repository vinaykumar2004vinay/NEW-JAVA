//IfElseIf Statements
public class three
{
    public static void main(String args[])
    {
        String city= "Tirupathi";
        
        if(city == "Hyderabad")
        {
            System.out.println("City Is Hyderabad");
        }

        else if(city == "Chennai")
        {
            System.out.println("City Is Chennai");
        }

        else if(city == "Tirupathi")
        {
            System.out.println("City Is Tirupathi");
        }

        else
        {
            System.out.println("There is no city");
        }

    }
}