//Changing an array element
class two
{
    public static void main(String args[])
    {
        int a[] = {21,5,7,6};       
        System.out.println("Before : " + a[0]);        //a[0]=33
        a[0] = 1452;                                   
        System.out.println("After : " + a[0]);         //a[0]=1452
    }
}